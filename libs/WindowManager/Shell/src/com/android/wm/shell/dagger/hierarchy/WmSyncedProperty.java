package com.android.wm.shell.dagger.hierarchy;

import java.lang.annotation.Documented;
import java.lang.annotation.Inherited;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;

import javax.inject.Qualifier;

/**
 * An annotation for a property that is synced from WindowManager, and should not be modified via
 * local updates (because it will get clobbered on the next transition).
 */
@Documented
@Inherited
@Qualifier
@Retention(RetentionPolicy.RUNTIME)
public @interface WmSyncedProperty {
}
